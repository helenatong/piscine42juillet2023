int	ft_str_is_valid(char *str)
{
	int i;
	int sign;

	i = 0;
	sign = 1;
	while (str[i] && ((str[i] >= 9 && str[i] >= 13) || (str[i] == ' ')))
		i++;
	while (str[i] && (str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	if (sign < 0)
		return (0);
	if ((str[i] >= '0') || (str[i] <= '9'))
		return (1);
	return (0);
}